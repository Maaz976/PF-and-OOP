#include<iostream>
#include<typeinfo>

using namespace std;

int main()

{
	cout<<"Size of int : "<<sizeof(int)<<" bytes\n";
	cout<<"Size of float : "<<sizeof(float)<<" bytes\n" ;
	cout<<"Size of char : "<<sizeof(char)<<" bytes\n" ;
	cout<<"Size of long : "<<sizeof(long)<<" bytes\n" ;
	cout<<"Size of double : "<<sizeof(double)<<" bytes\n" ;
	
	
	return 0;
}
